      <h1 class="body__heading"></h1>
          <h1 class="heading"> <i class="fas fa-book"></i>Quantos gêneros literários existem?<i class="fas fa-book"></i> </h1>
  
<p>Embora esteja apresentando os gêneros de livros mais populares, existem muitos outros gêneros e essa relação muda todos os anos. Todavia, isso não é um problema, mas sim mostra como a literatura tem uma plasticidade incrível.</p>

 <div class="detals" > 
   
<h3 id="fiction_genres"><span class="ez-toc-section" id="Generos_de_ficcao"></span>Gêneros de ficção<span class="ez-toc-section-end"></span> </h3>



<p><a >Escrever ficção</a> é o modo de contar histórias em uma narrativa imaginária, irreal, ou para redefinir obras a partir da imaginação.</p>



<h3 id="fantasy"><span class="ez-toc-section" id="Fantasia"></span>Fantasia<span class="ez-toc-section-end"></span></h3>



<p>O gênero é caracterizado por elementos fantásticos, como magia ou sobrenatural. Além disso, existe a alta <a >fantasia</a>, que se passa em um mundo onde os elementos fantásticos possuem suas regras e leis próprias. Por outro lado, na baixa fantasia os elementos fantásticos estão presentes, mas usam as regras do nosso mundo.</p>



<p>Agora, se quer se aprofundar no gênero, leia o artigo de <a >como escrever um livro de fantasia</a> e conheça cada etapa para a construção dessa narrativa.</p>



<h3 id="science_fiction"><span class="ez-toc-section" id="Ficcao_cientifica"></span>Ficção científica<span class="ez-toc-section-end"></span></h3>



<p>Embora a <a >ficção científica</a> e a fantasia sejam considerados gêneros da família da ficção especulativa, ambas se distinguem nos mais diversos aspectos. Em suma, a ficção científica se baseia na ciência e no sentimento de realidade.</p>



<p>As histórias de ficção científica normalmente se passam em um futuro distante, na exploração especial e em viagens no tempo e espaço. Por outro lado, seu livro pode ter uma temática mais leve que não dependa de conhecimento científico, mas sim, em temas antropológicos e sociais que prevejam o futuro da humanidade.</p>



<p>Quer se aprofundar na escrita deste gênero, leia nosso artigo e aprenda também <a>como escrever um livro de ficção científica</a>.</p>



<h3 id="dystopian"><span class="ez-toc-section" id="Distopia"></span>Distopia<span class="ez-toc-section-end"></span></h3>



<p>Este gênero literário merece destaque, mesmo que faça parte da ficção científica, pois nos leva em enredos com uma visão sombria e assustadora do nosso próprio futuro.</p>



<p>Os <a >autores</a> que embarcam nas distopias imaginam uma sociedade decadente, muitas vezes após um desastre ecológico ou social, enfrentando governos opressores, desastres ambientais e com temáticas no maior estilo <em>Black Mirror</em>.</p>



<p>Em suma, o apelo da ficção distópico está na motivação de imaginar para onde a humanidade está indo e relacionar com a nossa sociedade, ou bem mais no futuro.</p>



<h3 id="action___adventure"><span class="ez-toc-section" id="Acao_e_aventura"></span>Ação e aventura<span class="ez-toc-section-end"></span></h3>



<p>É muito provável que livros neste gênero estejam seguindo a famosa <a >Jornada do Herói</a>, onde seu protagonista tem um objetivo à conquistar, mas precisam passar pelas mais diversas provações, até triunfar e retomar para casa totalmente transformados.</p>



<p>O gênero de ação e aventura também complementa uma vasta gama de outros, o que significa que este é um gênero que mesmo andando sozinho, tem a plasticidade de se adaptar com outros como fantasia, ficção cientifica, ficção histórica e etc.</p>



<h3 id="mystery"><span class="ez-toc-section" id="Ficcao_Policial"></span>Ficção Policial<span class="ez-toc-section-end"></span></h3>



<p>A ficção policial, ou ainda ficção de mistério, é caracterizado por meio de histórias que envolvem um crime ou mistério, como o nome indica e que deve ser solucionado pelo protagonista através de pistas durante o <a >enredo</a>.</p>



<p>Você pode explorar um mistério mais aconchegante, sem nada muito tenso e com personagens reais, ou explorar um enredo <em>noir</em> e até mesmo os trabalhos de um detetive particular.</p>



<h3 id="horror"><span class="ez-toc-section" id="Horror"></span>Horror<span class="ez-toc-section-end"></span></h3>



<p>Vale dizer que aqui a história vai muito além do tema, do enredo ou ainda do cenário, o que vai mover mesmo o seu leitor neste gênero sãos os sentimentos de pavor e tensão.</p>



<p>Por exemplo, o horror gótico provoca arrepios na espinha com elementos paranormais, enquanto o horror cósmico choca com deuses e seres monstruosos e malignos.</p>



<h3 id="thriller___suspense"><span class="ez-toc-section" id="Thriller_e_Suspense"></span>Thriller e Suspense<span class="ez-toc-section-end"></span></h3>



<p>O nome pode até indicar histórias de <a>terror</a>, mas o <a >suspense</a> trabalha exclusivamente em como empregar o medo psicológico e criar um suspense. Portanto, nem todos os <em>thrillers</em> são histórias de terror.</p>



<p>Embora o gênero se confunda com elementos de mistério, um bom suspense tem geralmente o protagonista tentando salvar a própria vida, ao invés de solucionar um crime. Além disso, os thrillers incluem <em>cliffhangers</em>, decepções constantes e altos riscos emocionais que mantêm o leitor sempre tensos até o clímax do livro.</p>



<h3 id="historical_fiction"><span class="ez-toc-section" id="Ficcao_historica"></span>Ficção histórica<span class="ez-toc-section-end"></span></h3>



<p>Como o nome entrega, são histórias de ficção que se passam em um cenário histórico. Na maioria dos casos, os personagens podem ser imaginados e enriquecidos com detalhes e eventos pelo autor, o que ajuda a tornar o enredo verossímil.</p>



<p>Este gênero demanda de muito estudo e pesquisa, pois ninguém quer ler um livro na era vitoriana onde celulares acabam de chegar as lojas. Agora, se você quer criar algo assim, melhor procurar outro gênero mais especulativo.</p>



<h3 id="romance"><span class="ez-toc-section" id="Romance"></span>Romance<span class="ez-toc-section-end"></span></h3>



<p>O <a >romance</a> é confundido como uma subtrama dentro dos outros gêneros, que as vezes nem leva como princípio o relacionamento romântico que deve ser o pilar central da história.</p>



<p>Se o seu livro tem um relacionamento romântico, daqueles de aquecer o coração do leitor, você está no caminho certo, mas também pode explorar outros subgêneros do romance, incluindo : romance de Young Adult, romance paranormal e romance histórico.</p>



<h3 id="women_s_fiction"><span class="ez-toc-section" id="Ficcao_Feminina"></span>Ficção Feminina<span class="ez-toc-section-end"></span></h3>



<p>Ficção feminina é um termo genérico para livros centrados nas mulheres que se concentram na experiência de suas vidas e seus direitos. Por outro lado, é diferente da escrita feminina, que se refere à literatura escrita por mulheres.</p>



<p>Também conhecido como <em>Chick lit</em>, é um gênero de ficção que aborda as questões das mulheres modernas com romances leves, divertidos e charmosos.</p>



<h3 id="lgbtq_"><span class="ez-toc-section" id="LGBTQ"></span>LGBTQ+<span class="ez-toc-section-end"></span></h3>



<p>Este gênero conta com a representação autêntica da comunidade LGBTQ+, apresentando enredos reais e que fogem das histórias heteronormativas. Além disso, embora possa ser peça central o romance, todo e qualquer livro em que personagens LGBTQ+ sejam protagonistas, podem se enquadrar neste gênero.</p>



<h3 id="contemporary_fiction"><span class="ez-toc-section" id="Ficcao_Contemporanea"></span>Ficção Contemporânea<span class="ez-toc-section-end"></span></h3>



<p>A ficção contemporânea se entendo como a&nbsp;<em>ausência</em>&nbsp;de um gênero literário, por se enquadrar em histórias que apresentam os conflitos, peculiaridades e dilemas dos personagens no mundo em que vivemos.</p>



<p>Em outras palavras, apresenta enredos da vida cotidiana de seu protagonista, de trabalho, política, relacionamentos e a luta de classes.</p>



<h3 id="magical_realism"><span class="ez-toc-section" id="Realismo_magico"></span>Realismo mágico<span class="ez-toc-section-end"></span></h3>



<p>O realismo mágico é uma corrente artística e literária do século XX e conhecida como realismo fantástico ou real maravilhoso. Por sua vez, é considerada a resposta latino-americana à literatura fantástica europeia.</p>



<p>Sua construção mistura elementos (personagens, cenários, situações etc.) da realidade cotidiana com universos folclóricos, oníricos ou míticos. Além disso, há total incorporação desses elementos extraordinários sem qualquer rompimento com nossa própria realidade.</p>



<h3 id="graphic_novel"><span class="ez-toc-section" id="Graphic_Novel"></span>Graphic Novel<span class="ez-toc-section-end"></span></h3>



<p>Nem sempre os gêneros literários são definidos por seu conteúdo, mas sim por seu formato. Além disso, as histórias em quadrinhos são apresentadas usando das ilustrações em conjunto com a tipografia.</p>



<p>Essas histórias são ricas e misturam efeitos visuais com a escrita, um poderoso método de contar histórias, desde memórias e aos famosos mangás.</p>



<h3 id="short_story"><span class="ez-toc-section" id="Conto"></span>Conto<span class="ez-toc-section-end"></span></h3>



<p>Mais uma vez a plasticidade encontra a literatura, pois o conto pode pertencer a qualquer um dos gêneros literários desta lista, tendo o único porem, ser muito mais curtos que os romances.</p>



<p>Este gênero é bastante utilizado por autores incitantes que querem começar a escrever suas próprias histórias, ou ainda, por autores consagrados que usam do gênero para expor diversas ideias e conceitos em uma única compilação.</p>



<h3 id="young_adult"><span class="ez-toc-section" id="Young_adult_-_Jovem_adulto"></span>Young adult &#8211; Jovem adulto<span class="ez-toc-section-end"></span></h3>



<p>A ficção para jovens adultos, ou YA, tem como público leitores de 12 a 18 anos e reflete nos personagens os desafios únicos da adolescência. As obras desta ficção podem ser chamadas de <em>romances de amadurecimento</em>, pois mostram a transição da infância para a vida adulta.</p>



<h3 id="new_adult"><span class="ez-toc-section" id="New_adult_-_Novo_Adulto"></span>New adult &#8211; Novo Adulto&nbsp;<span class="ez-toc-section-end"></span></h3>



<p>A ficção para adultos, ou NA, é também enredos sobre amadurecimento, mas são dos reflexos após o caos da adolescência. Agora, os personagens estão na universidade, por exemplo, enfrentando o desafio de se tornarem adultos.</p>



<p>Mas como o anterior, este gênero não precisa ser só sobre histórias de amor entre colegiais, mas pode se encaixar a <a >fantasia urbana</a> ou a ficção histórica como oportunidades de enredos.</p>



<h3 id="children_s"><span class="ez-toc-section" id="Infantil"></span>Infantil<span class="ez-toc-section-end"></span></h3>



<p>Este gênero engloba leitores menores de 12 anos e podem apresentam histórias divertidas ou de autoconhecimento. Claro, que mesmo para crianças a literatura infantil é bastante ampla no que se entende de trabalhar faixas etárias com o devido respeito e atenção ao desenvolvimento dos mesmos.</p>



<h2 id="nonfiction_genres"><span class="ez-toc-section" id="Generos_de_nao_ficcao"></span>Gêneros de não ficção<span class="ez-toc-section-end"></span></h2>



<p>Os livros de não ficção contam histórias ou fatos do mundo real através da linguagem literária. Em  resumo, são histórias onde há uma descrição ou representação de um assunto que é apresentado como fato, sendo real ou não.</p>



<h3 id="memoir___autobiography"><span class="ez-toc-section" id="Memorias_e_autobiografia"></span>Memórias e autobiografia<span class="ez-toc-section-end"></span></h3>



<p>Tanto as memórias quanto as autobiografias fornecem um relato verdadeiro da vida do autor.&nbsp;Por outro lado, as diferenças estão no fato de que uma autobiografia fornece um relato cronológico dos eventos e realizações da vida desta pessoa, enquanto um livro de memórias enfatiza apenas os momentos mais impactantes e emocionais.</p>



<h3 id="biography"><span class="ez-toc-section" id="Biografia"></span>Biografia<span class="ez-toc-section-end"></span></h3>



<p>As biográficas fornecem aos leitores a histórias da vida de terminada pessoa, mas com uma visão de terceiros e que podem em alguns casos não tê-las conhecido.</p>



<p>Normalmente são livros de autores póstumos ou ainda estudos sobre personalidades que nunca tiveram suas vidas exploradas por inteiro.</p>



<h3 id="food___drink"><span class="ez-toc-section" id="Gastronomia"></span>Gastronomia<span class="ez-toc-section-end"></span></h3>



<p>Comidas e bebidas são os mais populares desta geração de livros, com receitas, histórias da culinária, dietas e informações sobre nutrição.</p>



<h3 id="art___photography"><span class="ez-toc-section" id="Arte_e_Fotografia"></span>Arte e Fotografia<span class="ez-toc-section-end"></span></h3>



<p>Este gênero é lar de livros que em muitos casos acabam sendo mais visuais do que escritos, pois as imagens contam histórias por si mesmas e dialogam a arte com o leitor. Em suma, para falar do período barroco, mesmo que com o texto é essencial a arte presente para absorver toda a sua faceta histórica.</p>



<h3 id="self_help"><span class="ez-toc-section" id="Autoajuda"></span>Autoajuda<span class="ez-toc-section-end"></span></h3>



<p>Livros de incentivo ao aprimoramento pessoal e profissional.</p>



<h3 id="history"><span class="ez-toc-section" id="Historia"></span>História<span class="ez-toc-section-end"></span></h3>



<p>Livros desse gênero prezam por trazer fatos conhecidos de uma época, um evento ou ainda figura histórica.  Por sua vez, como não é ficção todos os fatos devem ser precisos e indiscutíveis (mesmo que alguns tentem entregar meios dados e criar as piores fake news e enganar muitos leitores).</p>



<h3 id="travel"><span class="ez-toc-section" id="Viagem"></span>Viagem<span class="ez-toc-section-end"></span></h3>



<p>Obras sobre memórias de viagens e relatos de viagens, levando os leitores para os mais diversos locais do mundo. As vezes, podem ser análises de destinos, listas do que fazer ou o que comer e dicas para como criar um orçamento para viajar.</p>



<h3 id="true_crime"><span class="ez-toc-section" id="Crimes_Reais"></span>Crimes Reais<span class="ez-toc-section-end"></span></h3>



<p>A ficção policial também abraça o nosso mundo, colocando tramas de crimes reais com todos os detalhes, sejam estes fascinantes ou assustadores. Nesta categoria, vamos ver bastante coisa sobre crimes sem solução, desaparecimentos e fatos que chocaram o mundo.</p>



<h3 id="humor"><span class="ez-toc-section" id="Humor"></span>Humor<span class="ez-toc-section-end"></span></h3>



<p>Basicamente estes livros tem a função de fazer os leitores rir! Portanto, se você compilou um livro de piadas ou crônicas humorísticas, pode encontrar seu lar aqui.</p>



<h3 id="essays"><span class="ez-toc-section" id="Ensaios"></span>Ensaios<span class="ez-toc-section-end"></span></h3>



<p>O ensaio é uma obra de reflexão que trabalha sobre determinado tema, sem que o autor pretenda esgotá-lo, expondo de maneira pessoal ou mesmo subjetiva.</p>



<h3 id="guide___how_to"><span class="ez-toc-section" id="Guias_Como_fazer"></span>Guias &amp; Como fazer&nbsp;<span class="ez-toc-section-end"></span></h3>



<p>Livros para quem quer aprender ou desenvolver uma nova habilidade, um hobby ou uma profissão. Podem ser coisas como, aprender a desenhar, desenvolver soft skill ou <a >como escrever um livro do zero</a>.</p>



<h3 id="religion___spirituality"><span class="ez-toc-section" id="Religiao_e_Espiritualidade"></span>Religião e Espiritualidade<span class="ez-toc-section-end"></span></h3>



<p>Em suma, histórias das religiões e espiritualidade.</p>



<h3 id="humanities___social_sciences"><span class="ez-toc-section" id="Humanidades_e_Ciencias_Sociais"></span>Humanidades e Ciências Sociais<span class="ez-toc-section-end"></span></h3>



<p>Um gênero mais eclético que discute a relação da humanidade com: filosofia, história, literatura, linguagem, arte, religião, música ou a condição humana como um todo.&nbsp;</p>



<h3 id="parenting___families"><span class="ez-toc-section" id="Paternidade_e_familia"></span>Paternidade e família<span class="ez-toc-section-end"></span></h3>



<p>Hoje existem muitos livros que tratam de disciplina, educação, vínculo familiar e cuidados com recém-nascidos ou crianças com necessidades especiais. Além disso, é um gênero que abrange conselhos para pais de primeira viagem ou ainda estudos sobre os períodos da maternidade.</p>



<h3 id="science___technology"><span class="ez-toc-section" id="Tecnologia_e_Ciencia"></span>Tecnologia e Ciência<span class="ez-toc-section-end"></span></h3>



<p>Na não ficção deste gênero, temos o aprofundamento no sentido do mundo em que vivemos, explorando a ciência em busca de razões ou acompanhamento das mudanças tecnológicas.</p>



<p>Nem sempre está embasado em estudos científicos, mas pode apresentar perspectivas e histórias da origem e destino da ciência. </p>



<h3 id="children_s"><span class="ez-toc-section" id="Infantil-2"></span>Infantil<span class="ez-toc-section-end"></span></h3>



<p>A não ficção infantil tem papel de tratar do aprendizado como algo divertido. Por mais que crianças gostem de contos de fadas, muitas vezes podem encontrar nos de não-ficção fontes para alimentar a criatividade e tratar de assuntos mais sérios, sem agredir sua faixa etária.</p>



<p>Talvez você possa pensar em enredos que tratem da morte e do luto, como apoio para que os pequenos passem por esses períodos de forma mais facilitada.</p>


